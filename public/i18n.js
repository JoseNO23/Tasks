export function normalizeLocale(value) {
  return String(value || "").toLowerCase().startsWith("es") ? "es" : "en";
}

export function getStoredLocale() {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored) {
      return normalizeLocale(stored);
    }
  } catch {
    return DEFAULT_LOCALE;
  }
  return normalizeLocale(globalThis.navigator?.language) || DEFAULT_LOCALE;
}

export function setStoredLocale(locale) {
  localStorage.setItem(STORAGE_KEY, normalizeLocale(locale));
}

export function translate(locale, key, params = {}) {
  const lang = normalizeLocale(locale);
  const value = messages[lang][key] ?? messages[DEFAULT_LOCALE][key] ?? key;
  if (typeof value === "function") {
    return value(params);
  }
  return value.replace(/\{(\w+)\}/g, (_, token) => String(params[token] ?? ""));
}

export function createTranslator(locale) {
  return (key, params) => translate(locale, key, params);
}

export function formatDateTime(locale, value) {
  if (!value) {
    return "";
  }

  return new Intl.DateTimeFormat(normalizeLocale(locale) === "es" ? "es-PE" : "en-US", {
    dateStyle: "medium",
    timeStyle: "short",
  }).format(new Date(value));
}

export function applyStaticTranslations(root, t) {
  root.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = t(element.dataset.i18n);
  });

  root.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    element.setAttribute("placeholder", t(element.dataset.i18nPlaceholder));
  });

  root.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    element.setAttribute("aria-label", t(element.dataset.i18nAriaLabel));
  });
}
